#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	base_hexa(char c)
{
	char	*base;

	base = "0123456789abcdef";
	ft_putchar(base[c / 16]);
	ft_putchar(base[c % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] < ' ' || str[i] > '~')
		{
			write(1, "\\", 1);
			base_hexa(str[i]);
		}
		else
			ft_putchar(str[i]);
		i++;
	}
}
